    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
        Boolean isValid;
        isValid = ValidChecks();

        if (isValid == true) {
            String Password = new String(txt_password.getPassword());
            String PasswordConfirm = new String(txt_cfmPassword.getPassword());
            //If password is entered correctly twice
            if (Password.equals(PasswordConfirm)) {
                ArrayList UserRegister = new ArrayList();

                UserRegister.add(txt_username.getText());
                UserRegister.add(Password);
                UserRegister.add(txt_firstName.getText());
                UserRegister.add(txt_lastName.getText());
                UserRegister.add(txt_email.getText());

                if (cbx_classical.isSelected()) {
                    UserRegister.add("Classical");
                }

                if (cbx_dance.isSelected()) {
                    UserRegister.add("Dance");
                }

                if (cbx_folk.isSelected()) {
                    UserRegister.add("Folk");
                }
                if (cbx_funk.isSelected()) {
                    UserRegister.add("Funk");
                }
                if (cbx_hiphop.isSelected()) {
                    UserRegister.add("HipHop");
                }

                if (cbx_indie.isSelected()) {
                    UserRegister.add("Indie");
                }

                if (cbx_jazz.isSelected()) {
                    UserRegister.add("Jazz");
                }
                if (cbx_pop.isSelected()) {
                    UserRegister.add("Pop");
                }
                if (cbx_reggae.isSelected()) {
                    UserRegister.add("Reggae");
                }
                if (cbx_rock.isSelected()) {
                    UserRegister.add("Rock");
                }
                if (cbx_soulRandB.isSelected()) {
                    UserRegister.add("SoulRandB");
                }
                if (cbx_techno.isSelected()) {
                    UserRegister.add("Techno");
                }

                //Get file path from file chooser               
                try {
                    FileInputStream UserPicture = new FileInputStream(Filepath);
                    byte[] buffer = new byte[UserPicture.available()];
                    UserPicture.read(buffer);

                    Socket NocturneServer = new Socket("localhost", 9090);

                    ObjectOutputStream OutToServer = new ObjectOutputStream(NocturneServer.getOutputStream());
                    ObjectInputStream InFromServer = new ObjectInputStream(NocturneServer.getInputStream());
                    Datapacket UserDetails = new Datapacket();

                    UserDetails.SetCommand("CREATE_USER");
                    UserDetails.SetArray(UserRegister);
                    UserDetails.SetFirstByte(buffer);

                    OutToServer.writeObject(UserDetails);

                    Datapacket Reply = (Datapacket) InFromServer.readObject();
                    OutToServer.close();
                    InFromServer.close();

                    NocturneServer.close();

                    if ("Registered".equals(Reply.GetData())) {
                        ClearForm();

                        JOptionPane.showMessageDialog(this,
                                "Your new account has been created, log in to use Nocturne",
                                "Registering",
                                JOptionPane.PLAIN_MESSAGE);

                        Login login = new Login();
                        login.setLocationRelativeTo(null);
                        login.setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this,
                                "This username belongs to another profile. Please choose another username.",
                                "Registering",
                                JOptionPane.PLAIN_MESSAGE);
                    }

                } catch (FileNotFoundException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                } catch (IOException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(this,
                        "Your passwords do not match.",
                        "Error",
                        JOptionPane.PLAIN_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_registerActionPerformed


    public ImageIcon ResizeImage(String ImagePath) {
        ImageIcon MyImage = new ImageIcon(ImagePath);
        Image img = MyImage.getImage();
        Image newImg = img.getScaledInstance(icon_profilePic.getWidth(), icon_profilePic.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(newImg);
        return image;
    }

    private Boolean ValidChecks() {

        if (txt_firstName.getText().length() < 2 || txt_lastName.getText().length() < 2) {
            JOptionPane.showMessageDialog(this,
                    "Names need to be at least two characters in length.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);

            return false;
        } else if (txt_email.getText().length() < 5 || txt_username.getText().length() < 5) {
            JOptionPane.showMessageDialog(this,
                    "Email and Usernames need to be a minimum of five characters long.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        } else if (txt_password.getPassword().toString().length() < 6) {
            JOptionPane.showMessageDialog(this,
                    "Passwords should be a minimum of six characters in length.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        } //if email does not contain a "@" or "."
        else if (txt_email.getText().contains("@") == false || txt_email.getText().contains(".") == false) {
            JOptionPane.showMessageDialog(this,
                    "Invalid email format.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        }

        int PrefCounter = 0;

        if (cbx_classical.isSelected()) {
            PrefCounter++;
        }

        if (cbx_dance.isSelected()) {
            PrefCounter++;
        }

        if (cbx_folk.isSelected()) {
            PrefCounter++;
        }
        if (cbx_funk.isSelected()) {
            PrefCounter++;
        }
        if (cbx_hiphop.isSelected()) {
            PrefCounter++;
        }

        if (cbx_indie.isSelected()) {
            PrefCounter++;
        }

        if (cbx_jazz.isSelected()) {
            PrefCounter++;
        }
        if (cbx_pop.isSelected()) {
            PrefCounter++;
        }
        if (cbx_reggae.isSelected()) {
            PrefCounter++;
        }
        if (cbx_rock.isSelected()) {
            PrefCounter++;
        }
        if (cbx_soulRandB.isSelected()) {
            PrefCounter++;
        }
        if (cbx_techno.isSelected()) {
            PrefCounter++;
        }

        if (PrefCounter < 1) {
            JOptionPane.showMessageDialog(this,
                    "You need to select at least one Genre.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);

            return false;
        }

        if ("".equals(Filepath)) {
            JOptionPane.showMessageDialog(this,
                    "You need to select a photo.",
                    "Error",
                    JOptionPane.PLAIN_MESSAGE);
            return false;
        }

        return true;
    }

